# for if Statement 
num = eval(input("Enter a number:- "))
if num%2==0:
    print("The Given number is even..")
else :
    print("The given number is odd number")

# For if elif else 
marks = eval(input("Enter the marks:- "))

if marks >= 90:
    print("first div")
elif marks >=80:
    print("second div")
elif marks >= 70:
    print("third div")
else :
    print("fourth div")